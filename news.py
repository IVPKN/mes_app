import tkinter as tk

def show_news_section(content_frame):

    for widget in content_frame.winfo_children():
        widget.destroy()
        
    canvas = tk.Canvas(content_frame, highlightthickness=0)
    scrollbar = tk.Scrollbar(content_frame, orient="vertical", command=canvas.yview)
    scrollable_frame = tk.Frame(canvas)
    
    canvas_window = canvas.create_window((0, 0), window=scrollable_frame, anchor="nw")
    
    scrollable_frame.bind(
        "<Configure>",
        lambda event: canvas.configure(scrollregion=canvas.bbox("all"))
    )
    
    canvas.configure(yscrollcommand=scrollbar.set)

    canvas.pack(side="left", fill="both", expand=True)
    scrollbar.pack(side="right", fill="y")

    def center_content(event=None):
        canvas_width = canvas.winfo_width()  
        canvas_height = canvas.winfo_height()  
        scrollable_frame_width = scrollable_frame.winfo_reqwidth()  
        scrollable_frame_height = scrollable_frame.winfo_reqheight()  
        
        x_offset = max((canvas_width - scrollable_frame_width) // 2, 0)
        y_offset = max((canvas_height - scrollable_frame_height) // 2, 0)
        
        canvas.coords(canvas_window, x_offset, y_offset)
    
    content_frame.bind("<Configure>", center_content)  
    canvas.bind("<Configure>", center_content)  
    
    tk.Label(
        scrollable_frame,
        text="Новости",
        font=("Arial", 20),
        anchor="center"
    ).pack(pady=20)

    for item in NEWS_DATA:
        title = item.get("title", "Без заголовка")
        content = item.get("content", "Нет данных для отображения")
        
        tk.Label(
            scrollable_frame,
            text=title,
            font=("Arial", 16),
            wraplength=600,
            fg="darkred",
            anchor="center",
            justify="center"
        ).pack(pady=10)

        tk.Label(
            scrollable_frame,
            text=content,
            font=("Arial", 14),
            wraplength=600,  
            justify="left", 
            anchor="w"  
        ).pack(pady=10)


